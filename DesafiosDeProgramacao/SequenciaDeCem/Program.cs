using System;

namespace SequenciaDeCem
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Imprimindo 100 números com for.");
            System.Console.WriteLine();

            for(int i = 0; i <= 100; i++)
            {
                System.Console.WriteLine(i);
            }
        }
    }
}
